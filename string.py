
a = "why fit in, when you are born to stand out!"

# Find the length of the string
b = len(a)
print("The length of the given string is", b)

# Check how many times the alphabet 'o' is occurring
print("The number of times 'o' is occurring is", a.count('o'))

# Convert the whole string into lower and upper cases
b = a.lower()
print(b)
y = a.upper()
print(y)

# Convert the string into a title
z = a.title()
print(z)

# Find the index number of "fit in"
index = a.find("fit in")
print("The index number of 'fit in' is", index)
